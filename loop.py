# Loop through a list
fruits = ["apple", "mango", "banana"]
for x in fruits :
    print(x, end=" ") # end=" " -> it prevents the code to go into the next line.

# Loop through a range of numbers
for i in range(5) :
    if i!=0 :
        print("Number : ", i)

# While Loop 
i = 0
while i < 5:
    i+=1
    print(i, " ", end=" ")